function money(value) {
  return Number(value || 0).toLocaleString('en-PH', {
    minimumFractionDigits: 2,
    maximumFractionDigits: 2
  });
}

function escapeCsv(value) {
  const text = String(value ?? '');
  const safe = /^[=+\-@]/.test(text) ? `'${text}` : text;
  if (safe.includes(',') || safe.includes('"') || safe.includes('\n')) {
    return `"${safe.replace(/"/g, '""')}"`;
  }
  return safe;
}

function titleFromFilename(filename) {
  return String(filename || 'Report')
    .replace(/\.[^.]+$/, '')
    .replace(/[-_]+/g, ' ')
    .trim() || 'Report';
}

function buildCsvReport(title, headers, rows) {
  const meta = getReportMetadata(title);
  const line = (values) => values.map(escapeCsv).join(',');
  return [
    line([meta.companyName]),
    line([meta.title]),
    line(['Generated', meta.generatedAt]),
    line(['Generated By', meta.generatedBy]),
    '',
    [headers, ...rows]
      .map((row) => row.map(escapeCsv).join(','))
      .join('\n'),
    '',
    ...meta.signatories.map((label) => line([label]))
  ].join('\n');
}

function wrapCsvTextReport(title, csvText) {
  const meta = getReportMetadata(title);
  const line = (values) => values.map(escapeCsv).join(',');
  return [
    line([meta.companyName]),
    line([meta.title]),
    line(['Generated', meta.generatedAt]),
    line(['Generated By', meta.generatedBy]),
    '',
    String(csvText || '').replace(/^\ufeff/, '').trimEnd(),
    '',
    ...meta.signatories.map((label) => line([label]))
  ].join('\n');
}

function downloadCsv(filename, headers, rows, title = titleFromFilename(filename)) {
  const csv = buildCsvReport(title, headers, rows);
  const blob = new Blob([`\ufeff${csv}`], { type: 'text/csv;charset=utf-8;' });

  // IE / old Edge
  if (window.navigator && window.navigator.msSaveOrOpenBlob) {
    window.navigator.msSaveOrOpenBlob(blob, filename);
    return;
  }

  const url = URL.createObjectURL(blob);
  const link = document.createElement('a');
  link.href = url;
  link.download = filename;
  link.rel = 'noopener';
  link.style.display = 'none';
  document.body.appendChild(link);
  link.click();
  link.remove();
  setTimeout(() => URL.revokeObjectURL(url), 0);
}

async function downloadFromApi(pathname, suggestedFilename = 'export.csv') {
  const response = await fetch(pathname, { credentials: 'include' });
  if (!response.ok) {
    let message = `Request failed (${response.status})`;
    try {
      const data = await response.json();
      message = data?.message || message;
    } catch {
      // ignore
    }
    throw new Error(message);
  }

  const disposition = response.headers.get('content-disposition') || '';
  const match = disposition.match(/filename="([^"]+)"/i);
  const filename = match?.[1] || suggestedFilename;
  const contentType = response.headers.get('content-type') || '';
  const isCsv = /\.csv$/i.test(filename) || contentType.toLowerCase().includes('csv');
  const blob = isCsv
    ? new Blob([`\ufeff${wrapCsvTextReport(titleFromFilename(filename), await response.text())}`], { type: 'text/csv;charset=utf-8;' })
    : await response.blob();

  // IE / old Edge
  if (window.navigator && window.navigator.msSaveOrOpenBlob) {
    window.navigator.msSaveOrOpenBlob(blob, filename);
    return;
  }

  const url = URL.createObjectURL(blob);
  const link = document.createElement('a');
  link.href = url;
  link.download = filename;
  link.rel = 'noopener';
  link.style.display = 'none';
  document.body.appendChild(link);
  link.click();
  link.remove();
  setTimeout(() => URL.revokeObjectURL(url), 0);
}

