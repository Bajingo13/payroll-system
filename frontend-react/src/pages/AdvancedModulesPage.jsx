function CompanyCalendar() {
  const { user } = useAuth();
  const role = normalizeRole(user?.role);
  const canManageCalendar = role === 'admin' || role === 'hr';
  const [month, setMonth] = useState(() => new Date().toISOString().slice(0, 7));
  const [events, setEvents] = useState([]);
  const [editingEventId, setEditingEventId] = useState(null);
  const [form, setForm] = useState({
    event_date: new Date().toISOString().slice(0, 10),
    title: '',
    event_type: 'Company Holiday',
    description: '',
    is_paid_holiday: true
  });
  const [message, setMessage] = useState('');
  const [loading, setLoading] = useState(false);
  const isHolidayEvent = (event) => /holiday|non-working/i.test(String(event.event_type || ''));

  const days = useMemo(() => {
    const [year, monthIndex] = month.split('-').map(Number);
    const count = new Date(year, monthIndex, 0).getDate();
    return Array.from({ length: count }, (_, index) => index + 1);
  }, [month]);
  const firstDayOffset = useMemo(() => {
    const [year, monthIndex] = month.split('-').map(Number);
    return new Date(year, monthIndex - 1, 1).getDay();
  }, [month]);
  const eventsByDay = useMemo(() => {
    return events.reduce((acc, event) => {
      const day = Number(String(event.event_date || '').slice(8, 10));
      if (!day) return acc;
      acc[day] = [...(acc[day] || []), event];
      return acc;
    }, {});
  }, [events]);
  const holidayCount = events.filter(isHolidayEvent).length;
  const paidHolidayCount = events.filter((event) => Number(event.is_paid_holiday) === 1).length;
  const payrollDeadlineCount = events.filter((event) => event.event_type === 'Payroll Deadline').length;

  async function loadEvents(targetMonth = month) {
    setLoading(true);
    setMessage('');

    try {
      const { data } = await api.get('/company-calendar/events', { params: { month: targetMonth } });
      if (!data.success) throw new Error(data.message || 'Unable to load company calendar.');
      setEvents(data.events || []);
    } catch (err) {
      setMessage(getApiMessage(err, 'Unable to load company calendar.'));
    } finally {
      setLoading(false);
    }
  }

  useEffect(() => {
    loadEvents(month).catch(() => {});
  }, [month]);

  function updateForm(field, value) {
    setForm((current) => ({ ...current, [field]: value }));
  }

  function startEdit(calEvent) {
    setEditingEventId(calEvent.event_id);
    setForm({
      event_date: calEvent.event_date,
      title: calEvent.title,
      event_type: calEvent.event_type,
      description: calEvent.description || '',
      is_paid_holiday: Number(calEvent.is_paid_holiday) === 1
    });
    setMessage(`Editing event: ${calEvent.title}`);
  }

  function cancelEdit() {
    setEditingEventId(null);
    setForm({
      event_date: new Date().toISOString().slice(0, 10),
      title: '',
      event_type: 'Company Holiday',
      description: '',
      is_paid_holiday: true
    });
    setMessage('');
  }

  async function saveEvent(event) {
    event.preventDefault();
    setLoading(true);
    setMessage('');

    try {
      const payload = {
        ...form,
        title: form.title.trim(),
        description: form.description.trim(),
        is_paid_holiday: form.is_paid_holiday ? 1 : 0
      };
      const { data } = editingEventId
        ? await api.put(`/company-calendar/events/${editingEventId}`, payload)
        : await api.post('/company-calendar/events', payload);
      if (!data.success) throw new Error(data.message || 'Unable to save calendar event.');
      cancelEdit();
      setMessage(editingEventId ? 'Calendar event updated.' : 'Calendar event added.');
      await loadEvents(month);
    } catch (err) {
      setMessage(getApiMessage(err, 'Unable to save calendar event.'));
    } finally {
      setLoading(false);
    }
  }

  async function deleteEvent(eventId) {
    setLoading(true);
    setMessage('');

    try {
      const { data } = await api.delete(`/company-calendar/events/${eventId}`);
      if (!data.success) throw new Error(data.message || 'Unable to delete calendar event.');
      setMessage('Calendar event deleted.');
      await loadEvents(month);
    } catch (err) {
      setMessage(getApiMessage(err, 'Unable to delete calendar event.'));
    } finally {
      setLoading(false);
    }
  }

  return (
    <>
      <section className="summary react-summary">
        <div className="card"><span>Calendar Items</span><strong>{events.length}</strong></div>
        <div className="card"><span>Company Holidays</span><strong>{holidayCount}</strong></div>
        <div className="card"><span>Paid Holidays</span><strong>{paidHolidayCount}</strong></div>
        <div className="card"><span>Payroll Deadlines</span><strong>{payrollDeadlineCount}</strong></div>
      </section>

      <section className="table-section">
        <div className="table-header">
          <div>
            <h3>Company Calendar</h3>
            <p>Track company holidays, special non-working days, payroll deadlines, and internal events.</p>
          </div>
          <input type="month" value={month} onChange={(event) => setMonth(event.target.value)} />
        </div>
        <div className="advanced-calendar">
          {WEEKDAY_NAMES.map((dayName) => (
            <div key={dayName} className="calendar-weekday">{dayName}</div>
          ))}
          {Array.from({ length: firstDayOffset }, (_, index) => (
            <div key={`blank-${index}`} className="calendar-blank" aria-hidden="true" />
          ))}
          {days.map((day) => {
            const dayEvents = eventsByDay[day] || [];
            const hasHoliday = dayEvents.some(isHolidayEvent);
            return (
              <div key={day} className={`calendar-day ${dayEvents.length ? (hasHoliday ? 'has-holiday' : 'has-event') : ''}`}>
                <strong>{day}</strong>
                {dayEvents.length ? (
                  <span>{dayEvents[0].event_type}</span>
                ) : (
                  <span>Open</span>
                )}
                {dayEvents.slice(0, 2).map((event) => (
                  <small key={event.event_id}>{event.title}</small>
                ))}
                {dayEvents.length > 2 ? <small>+{dayEvents.length - 2} more</small> : null}
              </div>
            );
          })}
        </div>
      </section>

      {canManageCalendar ? (
        <section className="table-section">
          <div className="table-header">
            <div>
              <h3>{editingEventId ? 'Edit Calendar Item' : 'Add Calendar Item'}</h3>
              <p>Use holidays for company-wide non-working dates and events for reminders or announcements.</p>
            </div>
          </div>
          <form className="report-filter-grid company-calendar-form" onSubmit={saveEvent}>
            <label>Date<input type="date" value={form.event_date} onChange={(event) => updateForm('event_date', event.target.value)} required /></label>
            <label>Title<input value={form.title} onChange={(event) => updateForm('title', event.target.value)} placeholder="Example: Independence Day" required /></label>
            <label>Type
              <select value={form.event_type} onChange={(event) => updateForm('event_type', event.target.value)}>
                <option>Company Holiday</option>
                <option>Special Non-Working Day</option>
                <option>Company Event</option>
                <option>Payroll Deadline</option>
                <option>Other</option>
              </select>
            </label>
            <label>Description<input value={form.description} onChange={(event) => updateForm('description', event.target.value)} placeholder="Optional notes" /></label>
            <label className="calendar-check">
              <input type="checkbox" checked={form.is_paid_holiday} onChange={(event) => updateForm('is_paid_holiday', event.target.checked)} />
              Paid holiday
            </label>
            <div className="toolbar">
              <button type="submit" className="btn" disabled={loading}>{loading ? 'Saving...' : editingEventId ? 'Update Item' : 'Add Item'}</button>
              {editingEventId ? <button type="button" className="btn secondary" onClick={cancelEdit}>Cancel</button> : null}
            </div>
          </form>
          {message ? <p className="message">{message}</p> : null}
        </section>
      ) : null}

      <section className="table-section">
        <h3>Calendar Items</h3>
        <div className="table-scroll">
          <table>
            <thead>
              <tr>
                <th>Date</th>
                {canManageCalendar ? <th>Actions</th> : null}
                <th>Title</th>
                <th>Type</th>
                <th>Paid</th>
                <th>Description</th>
              </tr>
            </thead>
            <tbody>
              {events.length === 0 ? <tr><td colSpan={canManageCalendar ? 6 : 5}>No calendar items for this month.</td></tr> : null}
              {events.map((event) => (
                <tr key={event.event_id}>
                  <td>{event.event_date}</td>
                  {canManageCalendar ? (
                    <td>
                      <div className="row-actions">
                        <button type="button" className="btn small-btn" disabled={loading} onClick={() => startEdit(event)}>Edit</button>
                        <button type="button" className="btn secondary small-btn" disabled={loading} onClick={() => deleteEvent(event.event_id)}>Delete</button>
                      </div>
                    </td>
                  ) : null}
                  <td>{event.title}</td>
                  <td><span className="status completed">{event.event_type}</span></td>
                  <td>{Number(event.is_paid_holiday) === 1 ? 'Yes' : 'No'}</td>
                  <td>{event.description || '-'}</td>
                </tr>
              ))}
            </tbody>
          </table>
        </div>
      </section>
    </>
  );
}

